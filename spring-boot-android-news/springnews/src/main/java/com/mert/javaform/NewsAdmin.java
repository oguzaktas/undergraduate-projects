    public Connection getConnection() throws SQLException, ClassNotFoundException {
        Connection conn;
        try {
            Class.forName("com.mysql.cj.jdbc.Driver");
            conn = DriverManager.getConnection("jdbc:mysql://localhost:3306/news?useSSL=false", "root", "123456");
            return conn;
        } catch (SQLException ex) {
            Logger.getLogger(NewsAdmin.class.getName()).log(Level.SEVERE, null, ex);
            JOptionPane.showMessageDialog(null, "<html><b>Veritabanina baglanti saglanamadi</b></html>");
        }
        return null;
    }

    public void addNews(String header, String content, String category, int likes, int dislikes, int views, String imageurl, Date publish_date) throws SQLException {
        Connection conn = null;
        try {
            conn = getConnection();
        } catch (SQLException | ClassNotFoundException ex) {
            Logger.getLogger(NewsAdmin.class.getName()).log(Level.SEVERE, null, ex);
        }
        PreparedStatement ps;
        try {
            ps = conn.prepareStatement("INSERT INTO `news`(header, content, category, likes, dislikes, views, imageurl, `publish_date`) VALUES(?, ?, ?, ?, ?, ?, ?, ?);");
            ps.setString(1, header);
            ps.setString(2, content);
            ps.setString(3, category);
            ps.setInt(4, likes);
            ps.setInt(5, dislikes);
            ps.setInt(6, views);
            ps.setString(7, imageurl);
            ps.setDate(8, tarih);
            ps.executeUpdate();
            JOptionPane.showMessageDialog(null, "<html><b>Haber eklendi.</b></html>");
        } catch (SQLException ex) {
            Logger.getLogger(NewsAdmin.class.getName()).log(Level.SEVERE, null, ex);
            JOptionPane.showMessageDialog(null, "<html><b>Kayit ekleme sirasinda hata olustu.</b></html>");
        }
    }

